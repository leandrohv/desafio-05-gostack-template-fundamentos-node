//   OUTCOME = 'outcome',
// }

class TransactionsRepository {
  private transactions: Transaction[];

  constructor() {
    this.transactions = [];
  }

  public all(): Transaction[] {
    return this.transactions;
  }

  // private calculateBalance(operator: Type): number {
  //   return this.transactions
  //     .filter(transaction => transaction.type === operator)
  //     .reduce((accumulator, transaction) => accumulator + transaction.value, 0);
  // }

  public getBalance(): Balance {
    /**
     * Annotation */
    const { income, outcome } = this.transactions.reduce(
      (accumulator: Balance, transaction: Transaction) => {
        switch (transaction.type) {
          case 'income':
            accumulator.income += transaction.value;
            break;
          case 'outcome':
            accumulator.outcome += transaction.value;
            break;
          default:
            break;
        }

        return accumulator;
      },
      {
        income: 0,
        outcome: 0,
        total: 0,
      },
    );

    // const income = this.calculateBalance(Type.INCOME);
    // const outcome = this.calculateBalance(Type.OUTCOME);
    const total = income - outcome;

    const balance: Balance = {
      income,
      outcome,
      total,
    };

    return balance;
  }

  public create({ title, value, type }: CreateTransactionDTO): Transaction {
    const transaction = new Transaction({ title, value, type });

    this.transactions.push(transaction);

    return transaction;
  }
}

export default TransactionsRepository;
